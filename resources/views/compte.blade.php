    <script>
      // Client Side Form Validation

      document.addEventListener("DOMContentLoaded", () => {
        // SELECTORS
        const emailForm = document.querySelector("#email");
        const phoneForm = document.querySelector("#Phone");
        const submitButton = document.querySelector("#submit-Button");
        const phoneAlerte = document.querySelector("#phoneAlert");
        const emailAlerte = document.querySelector("#emailAlert");
        if (!emailForm || !phoneForm || !submitButton) {
          return;
        }

        // VARIABLES
        let emailIsValid = false;
        let phoneIsValid = false;

        // HELPER FUNCTIONS
        const validateEmail = (value) => {
          const emailValidation =
            /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,})+$/;
          if (value.match(emailValidation)) {
            return true;
          }
          return false;
        };
        const validatePhone = (value) => {
          const phoneValidation = /\d{3}\s?\d{2}\s?\d{3}\s?\d{2}/;
          if (value.match(phoneValidation)) {
            return true;
          }
          return false;
        };
        const applyInvalidStyles = (el) => {
          el.classList.remove("input-valid-state2");
          el.classList.add("input-error-state2");
        };
        const applyValidStyle = (el) => {
          el.classList.remove("input-error-state2");
          el.classList.add("input-valid-state2");
        };
        const disableSubmitButton = () => {
          submitButton.disabled = true;
          submitButton.classList.add("submit-disabled-state2");
        };
        const enableSubmitButton = () => {
          submitButton.disabled = false;
          submitButton.classList.remove("submit-disabled-state2");
        };
        const updateSubmitButton = () => {
          if (emailIsValid && phoneIsValid) {
            enableSubmitButton();
          } else {
            disableSubmitButton();
          }
        };

        // EVENT LISTENERS
        emailForm.addEventListener("input", (event) => {
          const emailFormValue = event.target.value;
          if (validateEmail(emailFormValue)) {
            emailIsValid = true;
            applyValidStyle(emailForm);
            emailAlerte.style.display = "none";
          } else {
            emailIsValid = false;
            applyInvalidStyles(emailForm);
            emailAlerte.style.display = "block";
          }
          updateSubmitButton();
        });
        phoneForm.addEventListener("input", (event) => {
          const phoneFormValue = event.target.value;
          if (validatePhone(phoneFormValue)) {
            phoneIsValid = true;
            applyValidStyle(phoneForm);
            phoneAlerte.style.display = "none";
          } else {
            phoneIsValid = false;
            applyInvalidStyles(phoneForm);
            phoneAlerte.style.display = "block";
          }
          updateSubmitButton();
        });

        //EXECUTION
        updateSubmitButton();
      });
    </script>
